import React, { useEffect } from 'react';

export type ToastType = 'success' | 'error' | 'warning' | 'info';

interface ToastProps {
     message: string;
     type: ToastType;
     onClose: () => void;
     autoClose?: boolean;
     autoCloseDelay?: number;
     onRetry?: () => void;
}

const Toast: React.FC<ToastProps> = ({
     message,
     type,
     onClose,
     autoClose = true,
     autoCloseDelay = 5000,
     onRetry,
}) => {
     useEffect(() => {
          if (autoClose && type !== 'error') {
               const timer = setTimeout(() => {
                    onClose();
               }, autoCloseDelay);

               return () => clearTimeout(timer);
          }
     }, [autoClose, autoCloseDelay, onClose, type]);

     const getTypeStyles = () => {
          switch (type) {
               case 'success':
                    return {
                         bg: 'bg-green-900 bg-opacity-20',
                         border: 'border-green-700',
                         text: 'text-green-400',
                         icon: (
                              <svg className="w-5 h-5" fill="currentColor" viewBox="0 0 20 20">
                                   <path
                                        fillRule="evenodd"
                                        d="M10 18a8 8 0 100-16 8 8 0 000 16zm3.707-9.293a1 1 0 00-1.414-1.414L9 10.586 7.707 9.293a1 1 0 00-1.414 1.414l2 2a1 1 0 001.414 0l4-4z"
                                        clipRule="evenodd"
                                   />
                              </svg>
                         ),
                    };
               case 'error':
                    return {
                         bg: 'bg-red-900 bg-opacity-20',
                         border: 'border-red-700',
                         text: 'text-red-400',
                         icon: (
                              <svg className="w-5 h-5" fill="currentColor" viewBox="0 0 20 20">
                                   <path
                                        fillRule="evenodd"
                                        d="M10 18a8 8 0 100-16 8 8 0 000 16zM8.707 7.293a1 1 0 00-1.414 1.414L8.586 10l-1.293 1.293a1 1 0 101.414 1.414L10 11.414l1.293 1.293a1 1 0 001.414-1.414L11.414 10l1.293-1.293a1 1 0 00-1.414-1.414L10 8.586 8.707 7.293z"
                                        clipRule="evenodd"
                                   />
                              </svg>
                         ),
                    };
               case 'warning':
                    return {
                         bg: 'bg-yellow-900 bg-opacity-20',
                         border: 'border-yellow-700',
                         text: 'text-yellow-400',
                         icon: (
                              <svg className="w-5 h-5" fill="currentColor" viewBox="0 0 20 20">
                                   <path
                                        fillRule="evenodd"
                                        d="M8.257 3.099c.765-1.36 2.722-1.36 3.486 0l5.58 9.92c.75 1.334-.213 2.98-1.742 2.98H4.42c-1.53 0-2.493-1.646-1.743-2.98l5.58-9.92zM11 13a1 1 0 11-2 0 1 1 0 012 0zm-1-8a1 1 0 00-1 1v3a1 1 0 002 0V6a1 1 0 00-1-1z"
                                        clipRule="evenodd"
                                   />
                              </svg>
                         ),
                    };
               case 'info':
                    return {
                         bg: 'bg-blue-900 bg-opacity-20',
                         border: 'border-blue-700',
                         text: 'text-blue-400',
                         icon: (
                              <svg className="w-5 h-5" fill="currentColor" viewBox="0 0 20 20">
                                   <path
                                        fillRule="evenodd"
                                        d="M18 10a8 8 0 11-16 0 8 8 0 0116 0zm-7-4a1 1 0 11-2 0 1 1 0 012 0zM9 9a1 1 0 000 2v3a1 1 0 001 1h1a1 1 0 100-2v-3a1 1 0 00-1-1H9z"
                                        clipRule="evenodd"
                                   />
                              </svg>
                         ),
                    };
          }
     };

     const styles = getTypeStyles();

     return (
          <div className="fixed top-4 right-4 z-50 max-w-md animate-slide-in">
               <div className={`${styles.bg} border ${styles.border} rounded-lg p-4 shadow-lg`}>
                    <div className="flex items-start justify-between">
                         <div className="flex items-start gap-3 flex-1">
                              <div className={styles.text}>{styles.icon}</div>
                              <div className="flex-1">
                                   <p className={`${styles.text} text-sm font-medium mb-1 capitalize`}>
                                        {type}
                                   </p>
                                   <p className={`${styles.text} text-sm leading-relaxed`}>{message}</p>
                              </div>
                         </div>
                         <button
                              onClick={onClose}
                              className={`ml-4 ${styles.text} hover:opacity-75`}
                              aria-label="Close notification"
                         >
                              <svg
                                   className="w-5 h-5"
                                   fill="none"
                                   stroke="currentColor"
                                   viewBox="0 0 24 24"
                              >
                                   <path
                                        strokeLinecap="round"
                                        strokeLinejoin="round"
                                        strokeWidth={2}
                                        d="M6 18L18 6M6 6l12 12"
                                   />
                              </svg>
                         </button>
                    </div>
                    {onRetry && type === 'error' && (
                         <button
                              onClick={onRetry}
                              className={`mt-3 px-4 py-2 ${styles.bg} border ${styles.border} ${styles.text} text-sm font-medium rounded-lg hover:opacity-75`}
                         >
                              Retry
                         </button>
                    )}
               </div>
          </div>
     );
};

export default Toast;
